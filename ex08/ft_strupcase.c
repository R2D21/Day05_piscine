char	*ft_strupcase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		while (str[i] <= 32 || (str[i] >= 'A' && str[i] <= 'Z'))
			i++;
		str[i] = str[i] - 32;
		i++;
	}
	return(str);
}
